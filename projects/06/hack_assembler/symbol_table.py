class SymbolTable:

    def __init__(self):
        self.symbol_map = CORE_SYMBOLS
        self.symbol_counter = 16
        self.comp_map = _comp_map
        self.dest_map = _dest_map
        self.jmp_map = _jmp_map

    def get(self, symbol):
        """Retrieve the value of a symbol.
        If it does not yet exist, assign it the value of self.symbol_counter and increment self.symbol_counter."""
        if symbol not in self.symbol_map:
            self.symbol_map[symbol] = self.symbol_counter
            self.symbol_counter += 1
        return self.symbol_map[symbol]

    def add(self, symbol, value):
        """Add symbol with value value to symbol table."""
        if symbol in self.symbol_map:
            raise ValueError(f"symbol {symbol} already exists in map.")
        self.symbol_map[symbol] = value
